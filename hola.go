package main

import "fmt"

func main() {
	fmt.Println("¡Hola, Mundo desde Go!")
}

//* Todo programa en Go pertenece a un paquete (package)
//* package main es especial: indica que es un programa ejecutable, no una librería

// import sirve para incluir otros paquetes
// fmt es un paquete estándar de Go para formateo y printing (fmt = format)

// func declara una función
//main() es la función especial por donde siempre empieza la ejecución
